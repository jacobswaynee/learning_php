VARIABLES: a way to store info in order to reference it late.

it can be used to store names' numbers and all others in data.

variables must starts with a $ sign.

example:

<?php

   $age = 43;

?>

variable types:
integers: theses are whole numbers like 1 , 2 32..
floats:  these are numbers with decimals like 23.2 , 1.0..
characters: these are laters ; numbers or symblos
booleans : true or falls
strings : its a collection of characters 
